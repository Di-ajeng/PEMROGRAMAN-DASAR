package operator;


public class OperatorAritmatika {
 public static void main(String[] args) {
        System.out.println("Operasi Penjumlahan");
        int hasilPenjumlahan = 4 + 3;
        System.out.println("Hasil 4 + 3 = " + hasilPenjumlahan);
        System.out.println();
        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 7 - 0;
        System.out.println("Hasil 7 - 0 = " + hasilPengurangan);
        System.out.println();
        System.out.println("Operasi Pengalian");
        int hasilPengalian = 5 * 5;
        System.out.println("Hasil 5 * 5 = " + hasilPengalian);
        System.out.println();
        System.out.println("Operasi Pembagian");
        int hasilPembagian = 20 / 10;
        System.out.println("Hasil 20 / 10 = " + hasilPembagian);
        System.out.println();
        System.out.println("Operasi Habis bagi");
        int hasilSisa = 12 % 2;
        System.out.println("Hasil 12 % 2 = " + hasilSisa);
        int hasilSisaLain = 10 % 3;
        System.out.println("Hasil 10 % 3 = " + hasilSisaLain);
        System.out.println();
    }   
}
